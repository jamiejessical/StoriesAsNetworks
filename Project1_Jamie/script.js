function myFunction() {
    var x = document.getElementById("book1Open");
    var y = document.getElementById("book2Open");
    var z = document.getElementById("book3Open");
    var q = document.getElementById("book4Open");
    var a = document.getElementById("book0Open");
    var b = document.getElementById("book0");
    var c = document.getElementById("book");
    var d = document.getElementById("book3");
    var e = document.getElementById("book4");
    var f = document.getElementById("book2");
    var screenWidth = window.innerWidth;
    if (x.style.width === "0px") {
      if (screenWidth <= 900) {
          x.style.width = "63vw";
          b.style.display = "none";
          c.style.display = "none";
          d.style.display = "inline-flex";
          e.style.display = "inline-flex";
          f.style.display = "inline-flex";
      } else {
          x.style.width = "40vw";
      }
      y.style.width = "0px";
      z.style.width = "0px";
      q.style.width = "0px";
      a.style.width = "0px";
    } else {
      x.style.width = "0px";
      y.style.width = "0px";
      z.style.width = "0px";
      q.style.width = "0px";
      a.style.width = "0px";
      b.style.display = "inline-flex";
      c.style.display = "inline-flex";
      d.style.display = "inline-flex";
      e.style.display = "inline-flex";
      f.style.display = "inline-flex";
  }
}



